//! 923. 3Sum With Multiplicity

use std::collections::HashMap;
use std::hash::Hash;

const MOD: usize = 1_000_000_007;

impl Solution {
    pub fn three_sum_multi(arr: Vec<i32>, target: i32) -> i32 {
        let mut map: HashMap<i32, usize> = HashMap::new();
        for num in arr.iter() {
            match map.remove(num) {
                None => {
                    map.insert(*num, 1);
                }
                Some(val) => {
                    map.insert(*num, val + 1);
                }
            }
        }

        let mut keys: Vec<i32> = map.keys().cloned().collect();
        keys.sort();

        let mut result: usize = 0;

        for key1 in &keys {
            for key2 in &keys {
                // println!("{}+{}", key1, key2);
                if key1 > key2 { continue }
                let key3 = target - key1 - key2;
                if key3 < *key2 { break }
                // println!("{}+{}+{}", key1, key2, key3);
                match map.get(&key3) {
                    None => (),
                    Some(val) => {
                        if *key1 == key3 && key1 == key2 {
                            let freq = dbg!(map[key1]);
                            result += (freq * (freq - 1) * (freq - 2) / 6) % MOD
                        } else if key1 == key2 {
                            result += (map[key1] * (map[key2] - 1) / 2 * *val) % MOD
                        } else if *key2 == key3 {
                            result += (map[key1] * map[key2] * (map[&key3] - 1) / 2) % MOD
                        } else {
                            result += (map[key1] * map[key2] * *val) % MOD
                        }
                    }
                }
            }
        }

        result as i32
    }

}

